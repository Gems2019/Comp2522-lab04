package ca.bcit.comp2522.lab4;

/**
 * An interface that defines a method for reversing the details of an object.
 * Any class that implements this interface must provide an implementation for the backward method.
 *
 * @author Yuho Lim
 * @version 1.0
 */
public interface Reversible {

    /**
     * Reverses the details of an object.
     */
    void backward();
}
